const landscape = input.map(xAxis => xAxis.split(''));
// console.log(landscape);
const xRepeatOn = landscape[0].length;

// console.log(xRepeatOn);


function countTrees(lndscp, xMove, yMove) {
	let treeCount = 0, x = 0, y = 0;

	lndscp.forEach(xAxis => {
		// console.log(`coords: ${x}, ${y}`);
		// console.log(xAxis[x]);

		xAxis[x] === '#' ? treeCount++ : '';

		// if (xAxis[x] === '#') {
		// 	treeCount++;
		// }

		x+=xMove;
		y+=yMove;

		x >= xRepeatOn ? x%=xRepeatOn : x;
	});

	return treeCount;
}

