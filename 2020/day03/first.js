const landscape = input.map(xAxis => xAxis.split(''));
const xRepeatOn = landscape[0].length;

function countTrees(lndscp, xMove) {
	let treeCount = 0, x = 0;

	lndscp.forEach(xAxis => {
		// console.log(`coords: ${x}, ${y}`);
		if (xAxis[x] === '#') treeCount++;

		x+=xMove;
		if (x >= xRepeatOn) x%=xRepeatOn;
	});

	return treeCount;
}

